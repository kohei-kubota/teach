
//メニュー
$("#menuBtn").click(function(){
	  if($("#menu").hasClass("open")){
		  $("#menu").removeClass("open");
		  $("#menu").addClass("close");
	  }
	  else if($("#menu").hasClass("close")){
		  $("#menu").removeClass("close");
		  $("#menu").addClass("open");
	  }
});

$("#menuClose").click(function(){
	  if($("#menu").hasClass("open")){
		  $("#menu").removeClass("open");
		  $("#menu").addClass("close");
	  }
});

$("#menu li a").click(function(){
	  if($("#menu").hasClass("open")){
		  $("#menu").removeClass("open");
		  $("#menu").addClass("close");
	  }
});

